#include "so_long.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_printstr(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	if (str)
	{
		while (str[i])
		{
			ft_putchar(str[i]);
			i++;
		}
		ft_putchar('\n');
	}
	return (i);
}
